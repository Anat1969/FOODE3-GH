function HighlightText({ text, search }) {
  if (!search || search.length < 2) return <>{text}</>;
  const regex = new RegExp(`(${search.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')})`, 'gi');
  const parts = text.split(regex);
  return (
    <>
      {parts.map((part, i) =>
        regex.test(part) ? <mark key={i} className="search-highlight">{part}</mark> : part
      )}
    </>
  );
}

export default function PolicyViewer({ onClose }) {
  const [activeChapter, setActiveChapter] = useState('ch1');
  const [search, setSearch] = useState('');
  const contentRef = useRef(null);

  const searchResults = useMemo(() => {
    if (!search || search.length < 2) return null;
    const results = [];
    POLICY_SECTIONS.forEach(ch => {
      ch.paragraphs.forEach((p, pi) => {
        if (p.text.includes(search)) {
          results.push({ chapterId: ch.id, chapterTitle: ch.title, paraIndex: pi, text: p.text });
        }
      });
    });
    return results;
  }, [search]);

  const scrollToChapter = (id) => {
    setActiveChapter(id);
    const el = document.getElementById(`policy-${id}`);
    if (el) el.scrollIntoView({ behavior: 'smooth', block: 'start' });
  };

  const scrollToResult = (chapterId, paraIndex) => {
    setActiveChapter(chapterId);
    const el = document.getElementById(`para-${chapterId}-${paraIndex}`);
    if (el) {
      el.scrollIntoView({ behavior: 'smooth', block: 'center' });
      el.classList.add('flash');
      setTimeout(() => el.classList.remove('flash'), 2000);
    }
  };

  return (
    <div className="policy-overlay" onClick={onClose}>
      <div className="policy-modal glass-panel" onClick={e => e.stopPropagation()}>
        <div className="policy-header">
          <h2>הצבת פודטראק במרחב הציבורי – אשדוד</h2>
          <button className="ghost policy-close" onClick={onClose}>✕</button>
        </div>

        <div className="policy-search-bar">
          <input
            type="text"
            placeholder="חיפוש במסמך המדיניות..."
            value={search}
            onChange={e => setSearch(e.target.value)}
            className="policy-search-input"
          />
          {search && searchResults && (
            <span className="search-count">{searchResults.length} תוצאות</span>
          )}
        </div>

        <div className="policy-body">
          <aside className="policy-legend">
            <h3>תוכן עניינים</h3>
            <nav className="toc">
              {POLICY_SECTIONS.map(ch => {
                const subs = ch.paragraphs
                  .map((p, i) => ({ ...p, idx: i }))
                  .filter(p => p.bold && p.text.length > 8 && p.text.length < 80);
                return (
                  <div key={ch.id} className="toc-chapter">
                    <button
                      className={`toc-chapter-btn ${activeChapter === ch.id ? 'active' : ''}`}
                      onClick={() => scrollToChapter(ch.id)}
                    >
                      <span className="toc-dot" style={{ background: ch.color }} />
                      <span className="toc-num">{ch.num}.</span>
                      <span className="toc-title">{ch.title}</span>
                    </button>
                    {subs.length > 0 && (
                      <div className="toc-subs">
                        {subs.slice(0, 8).map((p, si) => {
                          const label = p.text.replace(/[:–\-]$/, '').trim();
                          const short = label.length > 45 ? label.slice(0, 45) + '…' : label;
                          return (
                            <button key={si} className="toc-sub-btn" onClick={() => scrollToResult(ch.id, p.idx)}>
                              <span className="toc-sub-num">{ch.num}.{si + 1}</span>
                              <span className="toc-sub-title">{short}</span>
                            </button>
                          );
                        })}
                      </div>
                    )}
                  </div>
                );
              })}
            </nav>

            {search && searchResults && searchResults.length > 0 && (
              <div className="search-results-legend">
                <h4>תוצאות חיפוש</h4>
                {searchResults.slice(0, 20).map((r, i) => (
                  <button key={i} className="search-result-btn" onClick={() => scrollToResult(r.chapterId, r.paraIndex)}>
                    <span className="sr-chapter">{r.chapterTitle.slice(0, 25)}</span>
                    <span className="sr-text">{r.text.slice(0, 60)}...</span>
                  </button>
                ))}
              </div>
            )}
          </aside>

          <div className="policy-text" ref={contentRef}>
            {POLICY_SECTIONS.map(ch => (
              <div key={ch.id} id={`policy-${ch.id}`} className="policy-chapter" style={{ background: ch.color + '0a', borderInlineStart: `4px solid ${ch.color}30`, borderRadius: 10, padding: '16px 20px', marginBottom: 32 }}>
                <h3 className="chapter-heading" style={{ borderColor: ch.color, color: ch.color }}>
                  <span className="chapter-num" style={{ background: ch.color }}>פרק {ch.num}</span>
                  <HighlightText text={ch.title} search={search} />
                </h3>
                {ch.paragraphs.map((p, pi) => (
                  <p
                    key={pi}
                    id={`para-${ch.id}-${pi}`}
                    className={`policy-para ${p.bold ? 'bold' : ''}`}
                  >
                    <HighlightText text={p.text} search={search} />
                  </p>
                ))}
              </div>
            ))}
          </div>
        </div>
      </div>
    </div>
  );
}
